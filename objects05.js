function isOdd(input) {

    if (input.length % 2 !== 0) {
        return false;

    } else return true;


}
console.log(isOdd([1, 2, 9, 2, 1, 5]));

function middleElement(array, frr) {
    if (fsrr(array)) {
        return "error"
    }


    var counter = 0;
    for (let index = 0; index < array.length / 2; index++) {
        counter++;

    }

    return counter;
}

